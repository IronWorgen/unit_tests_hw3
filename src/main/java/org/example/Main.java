package org.example;

public class Main {
    public static void main(String[] args) {}
    public static boolean evenOddNumber(int n) {
        if (n % 2 == 0) {
            return true;
        } else {
            return false;
        }
    }
    public static boolean numInRange(int n) {
        return n > 25 && n < 100;
    }



}